BasicUpstart2(start)
    *= $c000   //Start address of the program

start:  lda #$0f     //Set background color (black)
        sta $d020

        lda #$00     //Set border color (black)
        sta $d021

        ldx #$00     //Initialize X register

displayLoop:
        lda address, x   //Load PETSCII character from the given address
        jsr $ffd2        //CHROUT - Output character to the screen

        inx             //Increment X register

        cpx #$0400        //Check if reached end of graphics data (64 bytes)
        bne displayLoop //If not, continue displaying

waitForKey:
        jsr $f142       //GETIN - Wait for a keypress

        rts             //Return from subroutine and exit the program

address:
        .word data      //Point to the PETSCII graphics data

